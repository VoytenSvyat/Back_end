public class Example {
    public static int solution(int number) {
        ///  Задача: Если мы перечислим все натуральные числа ниже 10, которые кратны 3 или 5, мы получим 3, 5, 6 и 9.
        /// Сумма этих кратных равна 23.
        /// Завершите решение так, чтобы оно возвращало сумму всех кратных 3 или 5 чисел ниже переданного числа.
        /// Кроме того, если число отрицательное, верните 0.
        /// Примечание: если число кратно и 3, и 5, посчитайте его только один раз.
        if (number < 0) {
            return 0; // Если число отрицательное, возвращаем 0
        }

        int sum = 0;
        for (int i = 1; i < number; i++) {
            if (i % 3 == 0 || i % 5 == 0) {
                sum += i; // Учитываем только кратные 3 или 5
            }
        }

        return sum;
    }

    public static void main(String[] args) {
        int number = -1; // Вы можете заменить на любое число
        System.out.println("Сумма всех кратных 3 или 5 ниже " + number + " составляет: " + solution(number));
    }
}